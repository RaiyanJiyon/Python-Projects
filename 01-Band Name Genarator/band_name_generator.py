# Printing the welcome message
print("Welcome to the Band Name Generator.")

# Prompting the user to input the name of the city they grew up in
city_name = input("What Is the name of the city you grew up in?\n")

# Prompting the user to input their pet's name
pets_name = input("What's your pet's name?\n")

# Combining the city name and pet's name to generate the band name
band_name = city_name + " " + pets_name

# Printing the generated band name
print("Your band name could be " + band_name)
